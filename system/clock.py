def render_time(hhmm: str) -> str:
    rows = ["" for _ in range(7)]
    for ch in hhmm:
        digit = DIGITS.get(ch, ["       "] * 7)
        for i in range(7):
            rows[i] += digit[i] + "  "
    return "\n".join(rows)

def run(args=None):
    try:
        while True:
            os.system("clear")
            now = time.strftime("%H:%M")
            date = time.strftime("%Y-%m-%d")
            print("\033[96m" + render_time(now) + "\033[0m")
            print("\033[91m" + " " * 33 + date + "\033[0m")
            
            time.sleep(1)
    except KeyboardInterrupt:
        pass

if __name__ == "__main__":
    run()


